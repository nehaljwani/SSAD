$(document).ready(function() {
		$('#deleteRoom').hide();
		$('#addRoom').hide();
		$('#editRoom').show();
		$('#deleteBuilding').hide();
		$('#addBuilding').hide();
		$('#addroom').click(function(){
			$('#addRoom').show();
			$('#editRoom').hide();
			$('#deleteRoom').hide();
			$('#deleteBuilding').hide();
			$('#addBuilding').hide();
			$('#delbtn').hide();
			});
		$('#delroom').click(function(){
			$('#addRoom').hide();
			$('#editRoom').hide();
			$('#deleteRoom').show();
			$('#deleteBuilding').hide();
			$('#addBuilding').hide();
			$('#delbtn').hide();
			});
		$('#addbuild').click(function(){
			$('#addRoom').hide();
			$('#editRoom').hide();
			$('#deleteRoom').hide();
			$('#deleteBuilding').hide();
			$('#addBuilding').show();
			$('#delbtn').hide();
			});
		$('#delbuild').click(function(){
			$('#addRoom').hide();
			$('#deleteRoom').hide();
			$('#editRoom').hide();
			$('#deleteBuilding').show();
			$('#addBuilding').hide();
			$('#delbtn').hide();
			});
		$('#editroom').click(function(){
			$('#addRoom').hide();
			$('#editRoom').show();
			$('#deleteRoom').hide();
			$('#deleteBuilding').hide();
			$('#addBuilding').hide();
			$('#delbtn').hide();
			});
		})
